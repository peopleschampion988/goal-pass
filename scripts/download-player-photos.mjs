#!/usr/bin/env node
// Downloads player cutout photos from TheSportsDB into public/player_photos/
// and regenerates supabase/seed_players.sql from scripts/players-data.mjs.
//
// Usage: node scripts/download-player-photos.mjs
//
// Players whose photo can't be found keep photo_path = null in the seed
// (the UI falls back to an initials avatar). Re-running retries only misses.

import { mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { PLAYERS, slugOf } from "./players-data.mjs";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const photosDir = resolve(root, "public/player_photos");
const seedFile = resolve(root, "supabase/seed_players.sql");

const fold = (s) =>
  s
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .toLowerCase();

// "Paris Saint-Germain" should match API teams like "Paris SG".
function teamMatches(club, apiTeam) {
  if (!apiTeam) return false;
  const a = fold(club);
  const b = fold(apiTeam);
  if (a.includes(b) || b.includes(a)) return true;
  if (a.startsWith("paris") && b.startsWith("paris")) return true;
  if (a === "inter milan" && b.includes("inter")) return true;
  if (a === "ac milan" && b === "milan") return true;
  if (a === "athletic club" && b.includes("athletic")) return true;
  return false;
}

const sleep = (ms) => new Promise((r) => setTimeout(r, ms));

async function searchPlayer(name) {
  for (let attempt = 0; attempt < 3; attempt++) {
    try {
      const res = await fetch(
        `https://www.thesportsdb.com/api/v1/json/3/searchplayers.php?p=${encodeURIComponent(name)}`,
      );
      if (res.status === 429) {
        await sleep(5000 * (attempt + 1));
        continue;
      }
      if (!res.ok) throw new Error(`HTTP ${res.status}`);
      const json = await res.json();
      return json.player ?? [];
    } catch (err) {
      if (attempt === 2) throw err;
      await sleep(2000);
    }
  }
  return [];
}

function pickCandidate(candidates, search, club) {
  let best = null;
  let bestScore = -1;
  for (const c of candidates) {
    if (c.strSport !== "Soccer") continue;
    const url = c.strCutout || c.strRender || c.strThumb;
    if (!url) continue;
    let score = 0;
    if (fold(c.strPlayer ?? "") === fold(search)) score += 4;
    if (teamMatches(club, c.strTeam)) score += 2;
    if (c.strCutout) score += 1;
    if (score > bestScore) {
      bestScore = score;
      best = c;
    }
  }
  return best;
}

function extOf(url) {
  const m = /\.(png|jpg|jpeg|webp)(\?|$)/i.exec(url);
  return m ? `.${m[1].toLowerCase()}` : ".png";
}

function findExisting(slug) {
  return readdirSync(photosDir).find((f) => f.replace(/\.[^.]+$/, "") === slug) ?? null;
}

function writeSeed(photoBySlug) {
  const esc = (s) => s.replace(/'/g, "''");
  const rows = PLAYERS.map(([rank, name, search, position, country, club]) => {
    const photo = photoBySlug.get(slugOf(search));
    const photoSql = photo ? `'${esc(photo)}'` : "null";
    return `  ('${esc(name)}', '${position}', '${esc(country)}', '${esc(club)}', ${rank}, ${photoSql})`;
  });
  const sql = `-- 100 best players of 2026. Generated by scripts/download-player-photos.mjs — do not edit by hand.
-- photo_path matches files in public/player_photos/; null => initials avatar in the UI.
insert into players (name, position, country, club, rank, photo_path) values
${rows.join(",\n")}
on conflict (name) do update set
  position = excluded.position,
  country = excluded.country,
  club = excluded.club,
  rank = excluded.rank,
  photo_path = excluded.photo_path;
`;
  writeFileSync(seedFile, sql);
}

mkdirSync(photosDir, { recursive: true });

const photoBySlug = new Map();
const failed = [];

for (const [rank, name, search, , , club] of PLAYERS) {
  const slug = slugOf(search);
  const existing = findExisting(slug);
  if (existing) {
    console.log(`skip   ${existing} (exists)`);
    photoBySlug.set(slug, existing);
    continue;
  }
  try {
    const candidates = await searchPlayer(search);
    const pick = pickCandidate(candidates, search, club);
    if (!pick) {
      console.log(`MISS   #${rank} ${name}: no usable result`);
      failed.push(name);
      await sleep(1200);
      continue;
    }
    const url = pick.strCutout || pick.strRender || pick.strThumb;
    const file = `${slug}${extOf(url)}`;
    const res = await fetch(url);
    if (!res.ok) throw new Error(`image HTTP ${res.status}`);
    const buf = Buffer.from(await res.arrayBuffer());
    const isPng = buf.subarray(0, 4).toString("hex") === "89504e47";
    const isJpg = buf.subarray(0, 2).toString("hex") === "ffd8";
    if (!isPng && !isJpg) throw new Error("not a PNG/JPEG");
    writeFileSync(resolve(photosDir, file), buf);
    photoBySlug.set(slug, file);
    console.log(`ok     ${file}${pick.strCutout ? "" : " (thumb fallback)"}`);
  } catch (err) {
    console.log(`FAIL   #${rank} ${name}: ${err.message}`);
    failed.push(name);
  }
  await sleep(1200);
}

writeSeed(photoBySlug);
console.log(`\nphotos: ${photoBySlug.size}/${PLAYERS.length} · seed written to supabase/seed_players.sql`);
if (failed.length) {
  console.log("no photo (initials avatar will be used):");
  for (const name of failed) console.log(`  ${name}`);
}
